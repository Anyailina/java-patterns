package state;

/**
 * Каждая реализация интерфейса представляет конкретное состояние заказа
 * и определяет поведение для операций оплаты, отмены и доставки.
 */
public interface IOrderState {

    /**
     * Обрабатывает операцию оплаты заказа.
     * @param order заказ, для которого выполняется операция
     */
    void pay(Order order);

    /**
     * Обрабатывает операцию отмены заказа.
     * @param order заказ, для которого выполняется операция
     */
    void cancel(Order order);

    /**
     * Обрабатывает операцию доставки заказа.
     * @param order заказ, для которого выполняется операция
     */
    void deliver(Order order);

}
