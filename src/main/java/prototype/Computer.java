package prototype;

/**
 * Интерфейс для компьютеров, поддерживающих клонирование.
 */
public interface Computer {

    /**
     * Создает копию текущего компьютера.
     * @return новый экземпляр с такими же характеристиками
     */
    Computer clone();

    /**
     * Возвращает название компьютера.
     * @return название модели
     */
    String getName();

    /**
     * Возвращает модель процессора.
     * @return CPU компьютера
     */
    String getCpu();

    /**
     * Возвращает модель графического процессора.
     * @return GPU компьютера
     */
    String getGpu();

    /**
     * Возвращает объем оперативной памяти.
     * @return RAM в гигабайтах
     */
    Integer getRam();

    /**
     * Возвращает характеристики накопителя.
     * @return описание storage
     */
    String getStorage();

}
